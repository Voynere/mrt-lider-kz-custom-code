/** Подставляет название услуги вместо общей категории в текстах и FAQ. */
function mrt_finalize_service_content(array $content, string $display_name, string $si_category): array {
    $label = trim($display_name);
    if ($label === '' || $label === $si_category) {
        return $content;
    }

    $replacements = [
        'МРТ позвоночника' => $label,
        $si_category => $label,
    ];

    foreach (['what_is', 'preparation'] as $key) {
        if (empty($content[$key])) {
            continue;
        }
        foreach ($replacements as $from => $to) {
            $content[$key] = str_replace($from, $to, $content[$key]);
        }
    }

    if (!empty($content['faq'])) {
        foreach ($content['faq'] as &$item) {
            foreach ($replacements as $from => $to) {
                $item['q'] = str_replace($from, $to, $item['q']);
                $item['a'] = str_replace($from, $to, $item['a']);
            }
        }
        unset($item);
    }

    return $content;
}

