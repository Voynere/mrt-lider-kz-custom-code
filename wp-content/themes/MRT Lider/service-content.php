/**
 * Возвращает массив long-tail блоков для service landing.
 * Фильтруется через 'mrt_service_longtail_blocks' для расширения из ACF / плагинов.
 *
 * @param string $si_category  — значение ACF si_category
 * @param string $service_type — название service_type term
 * @param string $city         — slug города
 * @param array  $svc_content  — результат mrt_get_service_content()
 * @param string $display_name — название конкретной услуги (si_oblast), если отличается от категории
 * @return array [['title' => '...', 'heading_level' => 'h2', 'content' => '...'], ...]
 */
function mrt_get_service_longtail_blocks(string $si_category, string $service_type, string $city, array $svc_content = [], string $display_name = '', int $service_price = 0, int $service_discount = 0): array {
    $cat = mb_strtolower(trim($si_category), 'UTF-8');
    $cities = function_exists('mrt_seo_get_cities') ? mrt_seo_get_cities() : [];
    $city_name = $cities[$city] ?? $city;
    $city_gen = function_exists('mrt_seo_city_genitive') ? mrt_seo_city_genitive($city) : $city;
    $label = $display_name !== '' ? $display_name : $si_category;
    $label_lower = mb_strtolower($label, 'UTF-8');

    $blocks = [];

    // ── 1. Стоимость ──
    $price_intro = "Актуальная стоимость {$label} в медицинском центре МРТ Лидер {$city_gen}.";
    if (preg_match('/(кт|компьютерн)/u', $cat)) {
        $price_intro .= " Цены на компьютерную томографию в {$city_gen} — от 2 500 ₽. Скидки пенсионерам и льготникам.";
    } elseif (preg_match('/(узи|ультразвук)/u', $cat)) {
        $price_intro .= " Ультразвуковое исследование в {$city_gen} — от 1 500 ₽. Доступные цены, результат за 15 минут.";
    } elseif (preg_match('/(денситометр|остеопороз)/u', $cat)) {
        $price_intro .= " Денситометрия костей в {$city_gen} — от 1 800 ₽. Исследование плотности костной ткани.";
    } else {
        $branch_features = function_exists('mrt_city_branch_features') ? mrt_city_branch_features($city) : ['oms' => true, 'dms' => true];
        $insurance_suffix = function_exists('mrt_city_insurance_price_suffix') ? mrt_city_insurance_price_suffix($branch_features) : ', ОМС, ДМС';
        $discount_phrase = function_exists('mrt_city_discount_marketing_phrase') ? mrt_city_discount_marketing_phrase($city) : 'Скидки до 30%';
        $currency = function_exists('mrt_get_kz_cities') && in_array($city, mrt_get_kz_cities(), true) ? '₸' : '₽';
        $effective_price = 0;
        if ($service_price > 0 && $service_discount > 0) {
            $effective_price = min($service_price, $service_discount);
        } elseif ($service_price > 0) {
            $effective_price = $service_price;
        } elseif ($service_discount > 0) {
            $effective_price = $service_discount;
        }
        $is_spine = preg_match('/(позвоноч|спин|шейн|грудн|пояснич|кресцов|копчик|крестц)/u', $cat)
            || preg_match('/(позвоноч|спин|шейн|грудн|пояснич|кресцов|копчик|крестц)/u', $label_lower);
        $is_complex = preg_match('/(всего тела|комплексн|онкопоиск|пантомограф|программ)/u', $cat)
            || preg_match('/(всего тела|комплексн|онкопоиск|пантомограф|программ)/u', $label_lower);
        if ($effective_price > 0) {
            $price_intro .= ' Стоимость — от ' . number_format($effective_price, 0, '', ' ') . " {$currency}. {$discount_phrase}{$insurance_suffix}.";
        } elseif ($city === 'volgodonsk' && $is_spine) {
            $price_intro .= " МРТ позвоночника в {$city_gen} — от 4 500 ₽. {$discount_phrase}{$insurance_suffix}.";
        } elseif ($is_complex) {
            $price_intro .= " Стоимость комплексных программ в {$city_gen} — от 10 000 ₽. {$discount_phrase}{$insurance_suffix}.";
        } else {
            $price_intro .= " МРТ в {$city_gen} — от 3 000 ₽. {$discount_phrase}{$insurance_suffix}.";
        }
    }
    $price_intro .= " Запись онлайн или по телефону. Точные цены уточняйте у оператора — стоимость зависит от области исследования и наличия контраста.";
    $blocks[] = [
        'title' => "Стоимость {$label} в {$city_name}",
        'heading_level' => 'h2',
        'content' => $price_intro,
    ];

    /**
     * Фильтр для расширения / замены long-tail блоков.
     * Можно добавлять новые блоки из ACF или плагинов.
     *
     * @param array  $blocks       — текущие блоки
     * @param string $si_category  — категория услуги
     * @param string $service_type — тип услуги
     * @param string $city         — slug города
     */
    return apply_filters('mrt_service_longtail_blocks', $blocks, $si_category, $service_type, $city);
}
